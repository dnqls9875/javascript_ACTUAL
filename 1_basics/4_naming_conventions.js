/**
 * Naming Conventions
 *
 * 변수 이름 지을때 (식별자)
 * *1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용 할 수 있다.
 * *2) 특수기호는 언더스코어(_)와 달러($)를 사용 할 수 있다.
 * *3) 숫자로 이름을 시작 할 수 없다. ex) 1Name, 2Hello
 * *4) 키워드는 변수명으로 사용 할 수 없다. var, let, const, this, function, if, for.... 기타 등등
 */

let codefactory = "코드팩토리";
let $ive = "아이브";
const _yuJin = "안유진";

console.log(codefactory);
console.log($ive);
console.log(_yuJin);

