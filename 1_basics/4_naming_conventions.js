/**
 * Naming Convention 2
 *
 * *1) 카멜케이스 camelCase => 대부분의 언어에서 많이 사용 --> 변수이름 선언할 때 주로 사용할 것
 * *2) snake_case => 소문자 사용 띄어쓰기 시 언더바로 사용 --> 파일 이름 사용
 * *3) PascalCase => C# 마이크로소프트 계열의 언어에서 사용
 *
 */

const anYuJin = "안유진2"; // camelCase
console.log(anYuJin);
